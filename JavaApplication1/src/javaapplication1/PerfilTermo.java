    private void PreencheListObrigatorio(){
          try {
            // Conexão com o banco
            Connection conn = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/db2", "root", "senha");

            // ID do Termo que você quer listar (ex.: obrigatório ou opcional)
            int idTermo = 1;

            String sql = "SELECT idItemTermo, descricao FROM ItemTermo WHERE idTermo = ? ORDER BY idItemTermo ASC";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, idTermo);
            ResultSet rs = ps.executeQuery();

            // Preenche a lista de objetos
            while (rs.next()) {
                int id = rs.getInt("idItemTermo");
                String descricao = rs.getString("descricao");
                listaItensOBR.add(new ItemTermo(id,descricao));
            }

            rs.close();
            ps.close();
            conn.close();

        } catch (SQLException e) {
            e.printStackTrace();
        }

    
    }
    
