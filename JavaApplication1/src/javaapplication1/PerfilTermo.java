private void atualizarListaH() {
        modelo.clear();
        for (int i = 0; i < Habilidades.size(); i++) {
            modelo.addElement((i + 1) + " - " + Habilidades.get(i));
        }
    }

private void atualizarListaI() {
        modelo2.clear();
        for (int i = 0; i < Interesses.size(); i++) {
            modelo2.addElement((i + 1) + " - " + Interesses.get(i));
        }
    }
